def zh_count(s: str) -> int:
    return len(re.findall(r"[\u4e00-\u9fff]", s))


def pad_file(fp: Path, pads: list) -> None:
    text = fp.read_text(encoding="utf-8")
    m = re.search(r"(## [^\n]*案例详解\n)([\s\S]*?)(?=\n---\n\n## |\Z)", text)
    if not m:
        print(f"NO MATCH {fp.name}")
        return
    header, body = m.group(1), m.group(2)
    parts = re.split(r"(### 案例 \d+[^\n]+\n)", body)
    # parts: [maybe empty, title1, content1, title2, content2, ...]
    out = header
    case_idx = 0
    i = 1
    while i < len(parts):
        if parts[i].startswith("### 案例"):
            title = parts[i]
            content = parts[i + 1] if i + 1 < len(parts) else ""
            # strip trailing --- from content
            content = content.split("\n---")[0]
            pad = pads[case_idx] if case_idx < len(pads) else generic_pad("本案例")
            # add more generic if still short
            combined = content.rstrip() + pad
            while zh_count(combined) < 400 and case_idx < len(pads):
                combined += generic_pad("本课")
            out += title + combined.rstrip() + "\n\n---\n\n"
            case_idx += 1
            i += 2
        else:
            i += 1
    new_text = text[: m.start()] + out.rstrip() + "\n\n" + text[m.end() :]
    fp.write_text(new_text, encoding="utf-8")


def report():
    files = sorted(BASE.glob("第*课*.md"))
    files = [f for f in files if re.search(r"第(1[6-9]|2[0-9]|30)课", f.name)]
    rows = []
    for fp in files:
        text = fp.read_text(encoding="utf-8")
        m = re.search(r"## [^\n]*案例详解[\s\S]*?(?=\n---\n\n## |\Z)", text)
        if not m:
            continue
        section = m.group()
        titles = re.findall(r"### (案例 \d+[^\n]+)", section)
        parts = re.split(r"\n### 案例 \d+", section)[1:]
        stats = []
        for title, body in zip(titles, parts):
            t = body.strip().split("\n---")[0]
            stats.append((title, zh_count(t)))
        avg = sum(c for _, c in stats) / len(stats) if stats else 0
        under = sum(1 for _, c in stats if c < 400)
        rows.append((fp.name, len(stats), avg, under, stats))
    return rows


