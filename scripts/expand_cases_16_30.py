def apply_expansion(filepath: Path, new_section: str) -> bool:
    text = filepath.read_text(encoding="utf-8")
    pattern = r"(## [^\n]*案例详解\n)[\s\S]*?(?=\n---\n\n## |\Z)"
    m = re.search(pattern, text)
    if not m:
        print(f"  SKIP (no match): {filepath.name}")
        return False
    # new_section includes ## header
    if not new_section.startswith("## "):
        print(f"  SKIP (bad section): {filepath.name}")
        return False
    new_text = re.sub(pattern, new_section.rstrip() + "\n\n", text, count=1)
    filepath.write_text(new_text, encoding="utf-8")
    return True


def count_cases(section: str):
    titles = re.findall(r"### (案例 \d+[^\n]+)", section)
    parts = re.split(r"\n### 案例 \d+", section)[1:]
    stats = []
    for title, body in zip(titles, parts):
        text = body.strip()
        chars = len(re.sub(r"[#*_\-\s\n|]", "", text))
        stats.append((title, chars))
    return stats


